import '../../core/utils/design_utils.dart';

/// For Deciding BorderRadius
enum UIWidgetType {
  //*===========================================================================
  //*================================ Values ===================================
  //*===========================================================================
  button,
  card,
  textField;

  //*===========================================================================
  //*================================= Methods =================================
  //*===========================================================================
  double get radius {
    switch (this) {
      case UIWidgetType.textField:
        return DesignUtils.defaultTextFieldBorderRadiusValue;

      default:
        return DesignUtils.defaultBorderRadiusValue;
    }
  }
  //*===========================================================================
  //*===========================================================================
}
