class CheckboxModel {
  //*===========================================================================
  //*================================ Parameters ===============================
  //*===========================================================================
  final bool isSelected;
  final String label;
  final String? value;

  //*===========================================================================
  //*================================ Constructor ==============================
  //*===========================================================================

  const CheckboxModel({
    required this.isSelected,
    required this.label,
    this.value,
  });
  //*==================================================
  factory CheckboxModel.toggle(CheckboxModel oldItem) => CheckboxModel(
        isSelected: !oldItem.isSelected,
        label: oldItem.label,
        value: oldItem.value,
      );

  //*===========================================================================
  //*===========================================================================
}
